package ExerciciosArrays;

public class ex06 {
    // Exercício 3: Escreva um código
    // Java que exiba os valores de um
    // array com o nome a do tipo double
    // numa mesma linha.

    public static void main(String[] args) {
        double a[] = {13.4, 23, 9};
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
    }
}
